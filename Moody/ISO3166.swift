extension ISO3166.Country: LocalizedStringConvertible {
    public var localizedDescription: String {
        let loc = NSLocale.current as NSLocale
        return loc.displayName(forKey: NSLocale.Key.countryCode, value: threeLetterName) ?? NSLocalizedString("country.Unknown", tableName: nil, bundle: Bundle(for: Continent.self), value: "country.Unknown", comment: "")
    }
}

extension ISO3166.Continent: LocalizedStringConvertible {
    public var localizedDescription: String { return String(describing: self) }
}
